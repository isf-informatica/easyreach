router = APIRouter()

def generate_nginx_conf(school: School) -> str:
    return f"""worker_processes  1;
events {{ worker_connections 1024; }}
http {{
    include       mime.types;
    default_type  application/octet-stream;
    sendfile      on;
    keepalive_timeout 65;
    server {{
        listen {school.nginx_port};
        root {school.storage_path.replace(chr(92), '/')};
        autoindex off;
        location / {{
            limit_except GET HEAD OPTIONS {{ deny all; }}
            add_header Accept-Ranges bytes;
            add_header Cache-Control "public, max-age=3600";
            add_header Access-Control-Allow-Origin "*";
            try_files $uri $uri/ =404;
        }}
        location ~* \\.(mp4|webm|avi|mov|mkv)$ {{
            limit_except GET HEAD OPTIONS {{ deny all; }}
            add_header Accept-Ranges bytes;
            add_header Access-Control-Allow-Origin "*";
            mp4;
            mp4_buffer_size 1m;
            mp4_max_buffer_size 5m;
        }}
        location ~* \\.pdf$ {{
            limit_except GET HEAD OPTIONS {{ deny all; }}
            add_header Content-Type application/pdf;
            add_header Accept-Ranges bytes;
            add_header Access-Control-Allow-Origin "*";
            add_header Content-Disposition inline;
        }}
    }}
}}"""

def generate_sync_bat(school: School, folders: list) -> str:
    lines = [
        "@echo off",
        f"echo ======================================== >> {school.storage_path}\\sync_log.txt",
        f"echo Sync Started: %date% %time% >> {school.storage_path}\\sync_log.txt",
        ""
    ]
    for folder in folders:
        lines.append(f'REM {folder} sync')
        lines.append(
            f'D:\\MinIO\\rclone.exe --config "C:\\Users\\admin\\AppData\\Roaming\\rclone\\rclone.conf" '
            f'sync "aws-s3:easylearn1/{folder}" "{school.storage_path}\\{folder}" '
            f'--log-file={school.storage_path}\\sync_log.txt --log-level INFO --transfers 5 --retries 3'
        )
        lines.append("")
    lines.append(f"echo Sync Complete: %date% %time% >> {school.storage_path}\\sync_log.txt")
    return "\n".join(lines)

def generate_backup_bat(school: School) -> str:
    return f"""@echo off
echo ======================================== >> {school.storage_path}\\backup_log.txt
echo Backup Started: %date% %time% >> {school.storage_path}\\backup_log.txt
D:\\MinIO\\rclone.exe --config "C:\\Users\\admin\\AppData\\Roaming\\rclone\\rclone.conf" copy "{school.storage_path}" "godaddy-backup:/" ^
  --log-file={school.storage_path}\\backup_log.txt ^
  --log-level INFO ^
  --transfers 3 ^
  --tpslimit 2 ^
  --checkers 4 ^
  --retries 10 ^
  --retries-sleep 30s
echo Backup Complete: %date% %time% >> {school.storage_path}\\backup_log.txt"""

