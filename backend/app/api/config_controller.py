@router.get("/generate/{school_id}")
def generate_configs(
    school_id: int,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user)
):
    school = db.query(School).filter(School.id == school_id).first()
    if not school:
        raise HTTPException(status_code=404, detail="School not found")

    folders = ["easylearn-ncert", "NCERT Course video", "SAAR"]

    # Create ZIP in memory
    zip_buffer = io.BytesIO()
    with zipfile.ZipFile(zip_buffer, "w", zipfile.ZIP_DEFLATED) as zf:
        zf.writestr("nginx.conf", generate_nginx_conf(school))
        zf.writestr("sync_aws.bat", generate_sync_bat(school, folders))
        zf.writestr("backup.bat", generate_backup_bat(school))
        zf.writestr("install_agent.ps1", generate_install_agent(school))
        zf.writestr("README.txt", generate_readme(school))

    zip_buffer.seek(0)

    return StreamingResponse(
        zip_buffer,
        media_type="application/zip",
        headers={
            "Content-Disposition": f"attachment; filename=easyreach_{school.code}_setup.zip"
        }
    )